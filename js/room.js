//-----------------------------------------------------------------------------
// ** Room Class
//-----------------------------------------------------------------------------
class Room {
  //---------------------------------------------------------------------------
  // * Initialize Object
  //---------------------------------------------------------------------------
  constructor(roomId) {
    this.index = roomId;
    this.name = '';
    this.width = 0;
    this.height = 0;
  }
  //---------------------------------------------------------------------------
  // * Calculate Room Area
  //---------------------------------------------------------------------------
  calculateArea() {
    return (width * height).toFixed(2);
  }
};
